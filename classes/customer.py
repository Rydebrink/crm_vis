class Customer:
    def __init__(self, customer_id, name, status, www, phone, address, zip_code, city, deals):
        self.customer_id = customer_id
        self.name = name
        self.status = status
        self.www = www
        self.phone = phone
        self.address = address
        self.zip_code = zip_code
        self.city = city
        self.deals = deals
